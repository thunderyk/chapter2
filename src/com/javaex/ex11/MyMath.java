package com.javaex.ex11;

public class MyMath {

	private static final double PI = 3.14;
	
	public static int plus(int num1, int num2) {
		return num1+num2;
	}
	public static double plus(double num1, double num2) {
		return num1+num2;
	}
	public static double circle(double r) {
		return r*r*PI;
	}
}
